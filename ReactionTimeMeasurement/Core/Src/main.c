// Función para generar un número aleatorio en el rango dado [min, max]
uint64_t generarNumeroAleatorio(uint64_t min, uint64_t max) {
	if (min > max) {
		return -1; // Devuelve -1 si el rango no es válido
	}
	uint64_t numero = min + rand() % (max - min + 1);
	return numero;
}

void startBuzzer(uint16_t frequency) {
	flagBuzzer = 1;
	uint32_t arr = 1000000 / frequency;
	uint32_t pulse = arr / 2;
	__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pulse);
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	__HAL_TIM_SET_COUNTER(&htim6, 0); // Reinicia el contador
	HAL_TIM_Base_Start_IT(&htim6);
	contadorBuzzer = 0;
}
;

void endBuzzer(void) {
	flagBuzzer = 0;
	HAL_TIM_PWM_Stop(&htim1, TIM_CHANNEL_1);
	HAL_TIM_Base_Start_IT(&htim6);
	contadorBuzzer = 0;
}
